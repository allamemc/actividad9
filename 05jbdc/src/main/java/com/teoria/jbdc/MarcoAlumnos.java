    private void table_update() {
        int CC;
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con1 = DriverManager.getConnection("jdbc:mysql://localhost:3306/","root","");
            insert = con1.prepareStatement("SELECT * FROM javadatos.alumnos");
            ResultSet Rs = insert.executeQuery();
            
            ResultSetMetaData RSMD = Rs.getMetaData();
            CC = RSMD.getColumnCount();
            DefaultTableModel DFT = (DefaultTableModel) jTable1.getModel();
            DFT.setRowCount(0);

            while (Rs.next()) {
                Vector v2 = new Vector();
           
                for (int ii = 1; ii <= CC; ii++) {
                    v2.add(Rs.getString("idalumno"));
                    v2.add(Rs.getString("nombre"));
                    v2.add(Rs.getString("fecha"));
                    v2.add(Rs.getString("curso"));
                    v2.add(Rs.getString("acepta"));
                }
                DFT.addRow(v2);
            }
        } catch (Exception e) {
        }
    }
